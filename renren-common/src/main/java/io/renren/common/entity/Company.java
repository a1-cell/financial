package io.renren.common.entity;

import java.math.BigDecimal;

public class Company {
    private Integer id;
    private Integer userid;
    private String companyname;
    private String companyid;
    private String name;
    private String image;
    private Integer statues;
    private BigDecimal money;
    private String creattime;
    private String altertime;
    private String companyscope;
    private String address;
    private String creatdate;
    private String indates;
    private Integer num;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getUserid() {
        return userid;
    }

    public void setUserid(Integer userid) {
        this.userid = userid;
    }

    public String getCompanyname() {
        return companyname;
    }

    public void setCompanyname(String companyname) {
        this.companyname = companyname;
    }

    public String getCompanyid() {
        return companyid;
    }

    public void setCompanyid(String companyid) {
        this.companyid = companyid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public Integer getStatues() {
        return statues;
    }

    public void setStatues(Integer statues) {
        this.statues = statues;
    }

    public BigDecimal getMoney() {
        return money;
    }

    public void setMoney(BigDecimal money) {
        this.money = money;
    }

    public String getCreattime() {
        return creattime;
    }

    public void setCreattime(String creattime) {
        this.creattime = creattime;
    }

    public String getAltertime() {
        return altertime;
    }

    public void setAltertime(String altertime) {
        this.altertime = altertime;
    }

    public String getCompanyscope() {
        return companyscope;
    }

    public void setCompanyscope(String companyscope) {
        this.companyscope = companyscope;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getCreatdate() {
        return creatdate;
    }

    public void setCreatdate(String creatdate) {
        this.creatdate = creatdate;
    }

    public String getIndates() {
        return indates;
    }

    public void setIndates(String indates) {
        this.indates = indates;
    }

    public Integer getNum() {
        return num;
    }

    public void setNum(Integer num) {
        this.num = num;
    }
}
